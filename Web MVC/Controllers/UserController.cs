        public async Task<ActionResult> Index(ManageMessageId? message, string name)
        {
            ViewBag.StatusMessage =
                message == ManageMessageId.ChangePasswordSuccess ? "Ваш пароль изменен."
                : message == ManageMessageId.SetPasswordSuccess ? "Пароль задан."
                : message == ManageMessageId.SetTwoFactorSuccess ? "Настроен поставщик двухфакторной проверки подлинности."
                : message == ManageMessageId.Error ? "Произошла ошибка."
                : message == ManageMessageId.AddPhoneSuccess ? "Ваш номер телефона добавлен."
                : message == ManageMessageId.RemovePhoneSuccess ? "Ваш номер телефона удален."
                : "";

            ApplicationUser applicationUser = _db.Users.FirstOrDefault(user => user.UserName == name);
            if (applicationUser == null)
            {
                var userId = User.Identity.GetUserId();
                applicationUser = _db.Users.First(user => user.Id == userId);
            }

            var model = new IndexNewViewModel
            {
                Id = applicationUser.Id,
                Logins = await UserManager.GetLoginsAsync(applicationUser.Id),
                TwoFactor = await UserManager.GetTwoFactorEnabledAsync(applicationUser.Id),
                BrowserRemembered = await AuthenticationManager.TwoFactorBrowserRememberedAsync(applicationUser.Id),
                Email = applicationUser.Email,
                PhoneNumber = applicationUser.PhoneNumber,
                UserName = applicationUser.UserName,
                HasPassword = UserManager.FindByIdAsync(applicationUser.Id).Result.PasswordHash != null
            };
            return View(model);
        }

